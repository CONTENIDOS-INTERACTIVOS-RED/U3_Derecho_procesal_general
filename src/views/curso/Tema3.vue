      .div(titulo="Recurso de reposición")
        p Se interpone ante el mismo funcionario judicial que profirió la providencia. Tiene como finalidad solicitarle que revoque, aclare o modifique su decisión, generalmente un auto, antes de que cause efectos definitivos. Procede contra autos de sustanciación e interlocutorios que no sean apelables, y su interposición debe hacerse dentro de los tres días siguientes a la notificación de la providencia o inmediatamente si la decisión se profiere en audiencia. Este recurso es útil para corregir errores formales o de interpretación, sin necesidad de escalar el proceso ante un superior. Por ejemplo, si un juez niega la práctica de una prueba por considerarla impertinente, y se estima que su admisión es clave para la defensa, puede solicitarse la reposición con argumentos precisos para que el juez reconsidere su decisión. Es importante resaltar que este recurso no tiene efecto suspensivo.
      .div(titulo="Recurso de apelación")
        p Es el recurso ordinario más relevante, puesto que permite que un juez superior jerárquico revise la decisión adoptada en primera instancia. Procede contra sentencias de primera instancia, así como contra autos interlocutorios expresamente señalados en el Artículo 321 del C.G.P. Su objetivo es obtener una revisión integral del caso en los aspectos que hayan sido objeto de inconformidad. La apelación puede ser concedida en efecto devolutivo es decir la decisión sigue ejecutándose, aunque se tramite la apelación o en efecto suspensivo la decisión no produce efectos hasta que se resuelva la apelación, dependiendo de la naturaleza del asunto. Por ejemplo, una sentencia que declara la nulidad de un contrato, puede ser apelada para que el Tribunal revise si hubo una interpretación adecuada de los hechos y del derecho aplicable. El recurso debe sustentarse, bien sea en audiencia o por escrito, y su omisión puede llevar a su inadmisión (Ortells Ramos & Bellido Penadés, 2016).
      .div(titulo="Recurso de queja")
        p Cuando el juez niega indebidamente el recurso de apelación, por ejemplo, al considerar erróneamente que no procede o que fue extemporáneo, la parte interesada puede acudir al recurso de queja. Este recurso se presenta directamente ante el juez superior, solicitando que se le ordene al inferior, conceder la apelación denegada. La queja cumple una función garantista del acceso a la segunda instancia, asegurando que no se cierre injustificadamente la puerta a una revisión superior. Por ejemplo, si un juez de primera instancia considera que una sentencia sobre responsabilidad extracontractual no admite apelación, pese a que sí cumple los requisitos, la parte afectada puede interponer la queja ante el tribunal, para que se reconozca su derecho.
      .div(titulo="Recurso de súplica")
